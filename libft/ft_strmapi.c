#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*result;
	long long int	cursor;

	result = ft_strdup(s);
	if (!result)
		return (0);
	cursor = -1;
	while (cursor++, *s)
		*(result + cursor) = f((unsigned int)cursor, *(s++));
	return (result);
}
