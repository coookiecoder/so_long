#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	cursor;

	cursor = 0;
	if (dest == src)
		return (dest);
	while (cursor++, cursor <= n)
		*((char *) dest + cursor - 1) = *((char *) src + cursor - 1);
	return (dest);
}
