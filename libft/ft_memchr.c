#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	cursor;

	cursor = 0;
	while (cursor < n)
	{
		if (*((unsigned char *)s + cursor) == (unsigned char)c)
			return ((char *)s + cursor);
		cursor++;
	}
	return (0);
}
