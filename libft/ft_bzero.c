#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned int	cursor;

	cursor = 0;
	while (cursor < n)
	{
		*((unsigned char *) s + cursor) = 0;
		cursor++;
	}
	return (s);
}
